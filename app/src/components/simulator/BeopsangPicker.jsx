export default function BeopsangPicker({ value, onChange }) {
  const v = value || { name: null, tiers: { 실체: true, 의념: true, 진령: true } };
  const cur = v.name;

  function selectName(name) {
    if (cur === name) {
      // 같은 거 클릭 → 해제
      onChange({ name: null, tiers: { 실체: true, 의념: true, 진령: true } });
    } else {
      // 법상 선택 시 모든 재능 자동 활성화
      onChange({ name, tiers: { 실체: true, 의념: true, 진령: true } });
    }
  }

  // 급수별로 그룹화
  const byTier = [1, 2, 3, 4].map((g) => ({
    급수: g,
    items: 법상_LIST.filter((b) => b.급수 === g),
  }));

  return (
    <div className="space-y-2">
      <div className="text-sm font-semibold text-slate-200">
        🌀 법상 선택 {cur ? <span className="text-amber-400">— {cur} (재능 3종 모두 적용)</span> : <span className="text-slate-400 text-xs">(선택 안 함)</span>}
      </div>
      <div className="space-y-2">
        {byTier.map(({ 급수, items }) => (
          <div key={급수} className="flex items-center gap-2">
            <span className="text-xs text-slate-400 w-10 shrink-0">{급수}급</span>
            <div className="flex gap-1 flex-1">
              {items.map((item) => {
                const active = cur === item.name;
                return (
                  <HoverTooltip
                    key={item.name}
                    maxWidth={520}
                    className={item.color === 'cyan' ? 'border-cyan-600' : item.color === 'red' ? 'border-red-600' : 'border-amber-600'}
                    content={
                      <>
                        <div className="text-xs font-bold mb-1 text-amber-300">
                          {item.icon} {item.name} <span className="text-slate-400 font-normal">({급수}급, {item.계열})</span>
                        </div>
                        <div className="text-[12px] text-slate-200 leading-relaxed space-y-1">
                          <div><span className="font-bold text-yellow-300">[실체]</span> {법상_DESC[item.name].실체}</div>
                          <div><span className="font-bold text-yellow-300">[의념]</span> {법상_DESC[item.name].의념}</div>
                          <div><span className="font-bold text-yellow-300">[진령]</span> {법상_DESC[item.name].진령}</div>
                        </div>
                      </>
                    }
                  >
                    <button
                      onClick={() => selectName(item.name)}
                      className={`px-2 py-1 text-[11px] rounded border transition cursor-help flex-1 ${
                        active
                          ? (item.color === 'cyan' ? 'bg-cyan-600 border-cyan-400 text-white font-bold' :
                             item.color === 'red' ? 'bg-red-600 border-red-400 text-white font-bold' :
                             'bg-amber-600 border-amber-400 text-white font-bold')
                          : 'bg-slate-800 border-slate-700 text-slate-300 hover:bg-slate-700'
                      }`}
                    >
                      {item.icon} {item.name}
                    </button>
                  </HoverTooltip>
                );
              })}
            </div>
          </div>
        ))}
      </div>
    </div>
  );
}
